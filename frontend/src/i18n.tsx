const DICTS: Record<Lang, Record<keyof typeof TR, string>> = { tr: TR, en: EN };

export type TKey = keyof typeof TR;

export function getLang(): Lang {
  return (localStorage.getItem("kt_lang") as Lang) === "en" ? "en" : "tr";
}

interface Ctx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (k: TKey, vars?: Record<string, string | number>) => string;
}

const I18nCtx = createContext<Ctx>({ lang: "tr", setLang: () => {}, t: (k) => TR[k] });

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(getLang);

  useEffect(() => {
    localStorage.setItem("kt_lang", lang);
    document.documentElement.lang = lang;
  }, [lang]);

  const t = useCallback((k: TKey, vars?: Record<string, string | number>) => {
    let s = DICTS[lang][k];
    if (vars) for (const [key, v] of Object.entries(vars)) s = s.replace(`{${key}}`, String(v));
    return s;
  }, [lang]);

  return <I18nCtx.Provider value={{ lang, setLang, t }}>{children}</I18nCtx.Provider>;
}

export const useT = () => useContext(I18nCtx);

/* Kategori değerleri veri olarak Türkçe kalır; yalnızca görünüm çevrilir. */
export function catLabel(t: Ctx["t"], category: string): string {
  const key = `cat${category}` as TKey;
  return key in TR ? t(key) : category;
}
